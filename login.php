              }
        });
      });


      $("#verify_otp_btn").click(function(){
        var mobile = $("#mobile").val();
        var otp = $("#otp").val();
         $.ajax({
            type: "POST",
            url: "web/php/otp_verify.php",
            data:{ mobile : mobile,otp:otp,},
